#ifndef INC_GY521_H_
#define INC_GY521_H_

#include "i2c.h"
#include <stdint.h>

#define GYRO_AVERAGE_SIZE 10
extern int16_t gyro_x_buffer[GYRO_AVERAGE_SIZE];
extern int16_t gyro_y_buffer[GYRO_AVERAGE_SIZE];
extern int16_t gyro_z_buffer[GYRO_AVERAGE_SIZE];
extern uint8_t buffer_index;

// MPU6050 structure
typedef struct
{

    int16_t Accel_X_RAW;
    int16_t Accel_Y_RAW;
    int16_t Accel_Z_RAW;
    double Ax;
    double Ay;
    double Az;

    int16_t Gyro_X_RAW;
    int16_t Gyro_Y_RAW;
    int16_t Gyro_Z_RAW;
    double Gx;
    double Gy;
    double Gz;

    float Temperature;

    double KalmanAngleX;
    double KalmanAngleY;
} MPU6050_t;

// Kalman structure
typedef struct
{
    double Q_angle;
    double Q_bias;
    double R_measure;
    double angle;
    double bias;
    double P[2][2];
} Kalman_t;

uint8_t MPU6050_Init(I2C_HandleTypeDef *I2Cx);

void MPU6050_Read_Accel(I2C_HandleTypeDef *I2Cx, MPU6050_t *DataStruct);

void MPU6050_Read_Gyro(I2C_HandleTypeDef *I2Cx, MPU6050_t *DataStruct);

void MPU6050_Read_Temp(I2C_HandleTypeDef *I2Cx, MPU6050_t *DataStruct);

void MPU6050_Read_All(I2C_HandleTypeDef *I2Cx, MPU6050_t *DataStruct);

double Kalman_getAngle(Kalman_t *Kalman, double newAngle, double newRate, double dt);

double MPU6050_Moving_Average(uint16_t *buffer, uint16_t new_value);


#endif /* INC_GY521_H_ */
